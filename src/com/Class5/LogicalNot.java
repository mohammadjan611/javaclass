public class LogicalNot {

	public static void main(String[] args) {
		
		int num1=10;
		int num2=20;
		if (num1==num2) {
			System.out.println("Numbers are not equal");
		}else {
			System.out.println("Number are equal");
			
		}
		if (!(num1==num2)) {
			System.out.println("Numbers are not equal ");
		}else {
			System.out.println("Number are equal");
			
			
			
		}
		
		
		

	}

}
